package pattern;

public class RightAngledTriangleInvertedNumbers_II
{
    public static void main(String[] args)
    {
        int size = 6;
        for(int i=0;i<size;i++)
        {
            for(int j=i;j<size;j++)
            {
                System.out.print(i+1 + " ");
            }
            System.out.println();
        }
    }
}
